export const REGEX = {
  email:
    /^(([^<>()[\]\\.,;:\s@\"]+(\.[^<>()[\]\\.,;:\s@\"]+)*)|(\".+\"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
  name:  /^(?=[A-Za-z\s]{4,12}$)[A-Z][a-z]*(?:\s[A-Z][a-z]*){0,2}$/,
  username: /^[A-Za-z0-9_-]{4,16}$/,
  password: /^(?=.*[0-9])(?=.*[!-_@"£$%^&*])[A-Za-z0-9!-_@"£$%^&*]{8,32}$/,
  digit: /[^0-9]/g,
  decimal: /^\d*\.?\d+$/,
  letter: /[^a-zA-Z ]/,
  spaceLetter: /[^a-zA-Z]/,
};
